const FONT_W = 5, FONT_H = 7, FONT_SP = 1;
const FontCache = {}; // color -> {char: canvas}

function _fontGlyph(ch, color) {
  let bucket = FontCache[color];
  if (!bucket) bucket = FontCache[color] = {};
  if (bucket[ch]) return bucket[ch];
  const rows = FONT_GLYPHS[ch];
  if (!rows) return null;
  const c = makeCanvas(FONT_W, FONT_H);
  const g = c.getContext('2d');
  g.fillStyle = color;
  for (let y = 0; y < FONT_H; y++)
    for (let x = 0; x < FONT_W; x++)
      if (rows[y][x] === '#') g.fillRect(x, y, 1, 1);
  bucket[ch] = c;
  return c;
}

function drawText(ctx, str, x, y, color = '#fff', scale = 1) {
  str = String(str);
  let cx = Math.round(x);
  const cy = Math.round(y);
  for (let i = 0; i < str.length; i++) {
    const ch = str[i] === ' ' ? null : (FONT_GLYPHS[str[i]] ? str[i] : str[i].toUpperCase());
    if (ch) {
      const glyph = _fontGlyph(ch, color);
      if (glyph) ctx.drawImage(glyph, cx, cy, FONT_W * scale, FONT_H * scale);
    }
    cx += (FONT_W + FONT_SP) * scale;
  }
}

function textWidth(str, scale = 1) {
  return String(str).length * (FONT_W + FONT_SP) * scale - FONT_SP * scale;
}

function drawTextCentered(ctx, str, cx, y, color = '#fff', scale = 1) {
  drawText(ctx, str, cx - textWidth(str, scale) / 2, y, color, scale);
}

// Text with a 1px drop shadow for readability over scenery
function drawTextShadow(ctx, str, x, y, color = '#fff', scale = 1, shadow = 'rgba(0,0,0,0.6)') {
  drawText(ctx, str, x + scale, y + scale, shadow, scale);
  drawText(ctx, str, x, y, color, scale);
}
